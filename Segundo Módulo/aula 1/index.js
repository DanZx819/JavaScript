/**
 * Operadores de Comparação
 * 
 * > maior que
 * >= maior ou igual que
 * < menor que 
 * <= menor ou igual que
 * == igualdade (valor)
 * === igualdade (valor e tipo)
 * != diferença (valor)
 * !== diferença (valor e tipo)
 */

const comp = 10 > 5;
const comp2 = 10 <= 5;
const comp3 = 10 === 5;
const comp4 = 10 !== 5;


console.log(comp);
console.log(comp2);
console.log(comp3);
console.log(comp4);
